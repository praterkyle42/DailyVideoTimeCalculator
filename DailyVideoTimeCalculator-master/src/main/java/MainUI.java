    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
           // TODO add your handling code here:
           int inputMinutes = 0;
           int inputSeconds = 0;
           
           if(tryParseInt(txtMinutes.getText())) {
              inputMinutes = Integer.parseInt(txtMinutes.getText());
           }
           if(tryParseInt(txtSeconds.getText())) {
               inputSeconds = Integer.parseInt(txtSeconds.getText());
           }
           
           Video inputVideo = new Video(inputMinutes, inputSeconds);
           inputVideo.setVideoNumber(myVideoManager.getNumberOfVideos() + 1);
           myVideoManager.Create(inputVideo); 
           
           txtAddedMinutes.append("\n" + inputMinutes);
           txtAddedSeconds.append("\n" + inputSeconds);
           txtMinutes.setText(""); 
           txtSeconds.setText("");
           
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        
       
        
        int daysUntilTest = 0;  
        double totalDailyMinutes = 0;
        if(tryParseInt(txtDays.getText())) {
            daysUntilTest = Integer.parseInt(txtDays.getText());
        }
        else {
            JFrame error = new JFrame("Video Time Calculator");
            JOptionPane.showMessageDialog(error, "Please enter the number of days until your test");
        }
        double userVideosSum = myVideoManager.getVideoSum();
        userVideosSum = Math.ceil(userVideosSum/60);
        String test = String.valueOf(userVideosSum);
        lblOutputMinutes.setText(test);

        if(daysUntilTest == 0) {
            lblOutput.setText(test);
        }
        else {   
        totalDailyMinutes = Math.ceil(userVideosSum/daysUntilTest);
        String totalUserMinutes = String.valueOf(userVideosSum);
        String output = String.valueOf(totalDailyMinutes); 
        lblOutputMinutes.setText(totalUserMinutes); 
        lblOutput.setText(output); 
        }
    }//GEN-LAST:event_btnCalculateActionPerformed

