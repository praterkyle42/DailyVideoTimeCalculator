    public boolean tryParseInt(String value) {
        try {
            Integer.parseInt(value); 
            return true;
        } catch (NumberFormatException e) {
            return false; 
        }
    }
    public int getVideoSum(ArrayList<Video> video) {
        int sum = 0;
        int minutes;
        for(Video v : video) {
            minutes = v.getTotalSeconds();
            sum += minutes;
        }
        return sum;
    }

